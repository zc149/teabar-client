package routeTest.model;

public class Tea {
	
	private int id;
	private String name;
	private String size;
	private String price;
	
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getSize() {
		return size;
	}
	public String getPrice() {
		return price;
	}
	public Tea(int id, String name, String size, String price) {
		this.id = id;
		this.name = name;
		this.size = size;
		this.price = price;
	}

	
	
	
}
